import java.util.*;
public class AssignmentOperators {
	
	//Aritthmetic Assignment Operators
	//=        a=123       a=123
	//+=       a+=10       a=a+10
	//-=       a-=10       a=a-10
	//*=       a*=10       a=a*10
	///=       a/=10       a=a/10
	//%=       a%=10       a=a%10
	
	
	public static void main(String[]args) {
		int a=123;
		System.out.println(a);
		a+=10;
		System.out.println(a);
		a-=10;
		System.out.println(a);
		a*=10;
		System.out.println(a);
		a/=10;
		System.out.println(a);
		a%=10;
		System.out.println(a);
		
	}

}
